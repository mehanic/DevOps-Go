package main

import (
	"fmt"
)

func main() {
	// Declare and initialize variables
	var1 := 30
	var2 := 20

	// Perform arithmetic operations
	sum := var1 + var2
	diff := var1 - var2
	product := var1 * var2
	quotient := var1 / var2
	remainder := var1 % var2

	// Print the results
	fmt.Println(sum)
	fmt.Println(diff)
	fmt.Println(product)
	fmt.Println(quotient)
	fmt.Println(remainder)
}

